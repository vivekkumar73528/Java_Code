import java.util.*;
public class Arithmetic {
    public static void main(String args[]) {
        int A = 10;
        int B = 5;
        System.out.println("add = " + (A + B)); // Addition
        System.out.println("subtract = " + (A - B)); // Subtraction
        System.out.println("multiply = " + (A * B)); // Multiply
        System.out.println("division = " + (A / B)); // Division
        System.out.println("modulo(remainder) = " + (A % B)); // Module
    }

    
}
